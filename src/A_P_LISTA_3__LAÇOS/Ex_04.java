/*
Faça um programa que gere a seguinte série: 
10, 20, 30, 40, ..., 990, 1000. ​Use o laço FOR. 
 */
package A_P_LISTA_3__LAÇOS;

public class Ex_04 {

    public static void main(String[] args) {

        for (int i = 10; i <= 1000; i += 10) {
            
            System.out.println(i);
        }

    }
}
