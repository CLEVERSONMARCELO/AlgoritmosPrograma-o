/*
Escreva um programa que imprima todos os múltiplos de 7 menores que 200. ​Use o laço WHILE. 
 */
package A_P_LISTA_3__LAÇOS;

public class Ex_07 {

    public static void main(String[] args) {

        int i = 0;

        while (i < 200) {

            System.out.println(i);

            i = i + 7;
        }
    }
}
