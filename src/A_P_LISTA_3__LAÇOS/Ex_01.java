/*
 Faça um programa que mostre todos os números inteiros de 1 a 50. ​Use o laço WHILE. 
 */
package A_P_LISTA_3__LAÇOS;

public class Ex_01 {

    public static void main(String[] args) {

        int i = 1;

        while (i <= 50) {

            System.out.println(i);
            i++;
        }
        System.out.println("fim");

    }
}
