package A_P_Aula_07_repetição;

//Faça um programa que mostre os números pares entre 1 e 100, inclusive. 
// Use o laço while
 
public class Exerc_01 {

    public static void main(String[] args) {

        int i = 0;

        while (i<10) {
            System.out.println("teste " + i);

            i = i + 2;
        }
    }
}
